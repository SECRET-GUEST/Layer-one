#Click through the window

def set_clickthrough(hwnd):
    trough = win32gui.GetWindowLong(hwnd, win32con.GWL_EXSTYLE)
    trough = win32con.WS_EX_LAYERED | win32con.WS_EX_TRANSPARENT
    win32gui.SetWindowLong(hwnd, win32con.GWL_EXSTYLE, trough)
    win32gui.SetWindowLong(hwnd, win32con.GWL_EXSTYLE, trough)
    win32gui.SetLayeredWindowAttributes(hwnd, 0, 255, win32con.LWA_ALPHA)


